#!/usr/bin/env node

import { randomBytes } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const parametersPath = path.join(__dirname, "parameters.json");
const localParametersPath = path.join(__dirname, "parameters.local.json");
const hubDeployEnvPath = path.join(
  __dirname,
  "..",
  "..",
  "..",
  "..",
  "hub",
  ".env.deploy",
);

function randomBase64(bytes) {
  return randomBytes(bytes).toString("base64");
}

function randomHex(bytes) {
  return randomBytes(bytes).toString("hex");
}

function randomSigningKey(length) {
  const alphabet =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_!@#$%^&*+=";
  const bytes = randomBytes(length);
  let key = "";
  for (let i = 0; i < length; i += 1) {
    key += alphabet[bytes[i] % alphabet.length];
  }
  return key;
}

async function readResourcePrefix() {
  const parametersFile = await readFile(parametersPath, "utf8");
  const parameters = JSON.parse(parametersFile);
  const prefix = parameters?.parameters?.resource_prefix?.value ?? "eval-";
  return String(prefix);
}

function stripDashes(value) {
  return value.replace(/-/g, "");
}

async function main() {
  const resourcePrefix = await readResourcePrefix();
  const apiHost = `https://${resourcePrefix}endatix-api.azurewebsites.net`;
  const hubHost = `https://${resourcePrefix}endatix-hub.azurestaticapps.net`;
  const storageAccountName = `${stripDashes(resourcePrefix)}endatixstorage`;

  const authSecret = randomBase64(32);
  const sessionSecret = randomHex(32);
  const nextServerActionsEncryptionKey = randomBase64(32);
  const endatixJwtSigningKey = randomSigningKey(64);
  const submissionsAccessTokenSigningKey = randomSigningKey(64);
  const postgresAdminPassword = randomSigningKey(24);
  const initialUserEmail = "admin@endatix.com";
  const initialUserPassword = randomSigningKey(24);

  const localParameters = {
    $schema:
      "https://schema.management.azure.com/schemas/2019-04-01/deploymentParameters.json#",
    contentVersion: "1.0.0.0",
    parameters: {
      postgres_admin_password: {
        value: postgresAdminPassword,
      },
      initialUserEmail: {
        value: initialUserEmail,
      },
      initialUserPassword: {
        value: initialUserPassword,
      },
      endatixJwtSigningKey: {
        value: endatixJwtSigningKey,
      },
      submissionsAccessTokenSigningKey: {
        value: submissionsAccessTokenSigningKey,
      },
      hubSessionSecret: {
        value: sessionSecret,
      },
      hubAuthSecret: {
        value: authSecret,
      },
      nextServerActionsEncryptionKey: {
        value: nextServerActionsEncryptionKey,
      },
    },
  };

  await writeFile(
    localParametersPath,
    `${JSON.stringify(localParameters, null, 2)}\n`,
    "utf8",
  );

  const envDeployContent = [
    "# Generated by generate-quickstart-secrets.mjs",
    `ENDATIX_BASE_URL=${apiHost}`,
    "ENDATIX_API_PREFIX=/api",
    `SESSION_SECRET=${sessionSecret}`,
    `AUTH_SECRET=${authSecret}`,
    `NEXT_SERVER_ACTIONS_ENCRYPTION_KEY=${nextServerActionsEncryptionKey}`,
    "AUTH_TRUST_HOST=true",
    `AUTH_URL=${hubHost}`,
    `HUB_ADMIN_USERNAME=${initialUserEmail}`,
    "HUB_ADMIN_PASSWORD=",
    "SESSION_MAX_AGE_IN_MINUTES=900",
    `NEXT_PUBLIC_API_URL=${apiHost}/api`,
    "NEXT_PUBLIC_ENVIRONMENT=production",
    "NEXT_PUBLIC_IS_DEBUG_MODE=false",
    "NEXT_FORMS_COOKIE_NAME=FPSK",
    "NEXT_FORMS_COOKIE_DURATION_DAYS=7",
    `AZURE_STORAGE_ACCOUNT_NAME=${storageAccountName}`,
    "AZURE_STORAGE_ACCOUNT_KEY=",
    "AZURE_STORAGE_IS_PRIVATE=false",
    "",
  ].join("\n");

  await mkdir(path.dirname(hubDeployEnvPath), { recursive: true });
  await writeFile(hubDeployEnvPath, envDeployContent, "utf8");

  const greenCheck = "\x1b[32m✓\x1b[0m";
  const cyanInfo = "\x1b[36mℹ\x1b[0m";
  const yellowWarn = "\x1b[33m⚠\x1b[0m";

  console.log("\n🔐 Endatix Azure quickstart secrets generated:");
  console.log(`  ${greenCheck} ${localParametersPath}`);
  console.log(`  ${greenCheck} ${hubDeployEnvPath}`);
  console.log("");
  console.log(`${cyanInfo} Next steps`);
  console.log("  1) Review generated values and rotate any secrets if needed.");
  console.log(
    "  2) Fill missing values (for example AZURE_STORAGE_ACCOUNT_KEY and HUB_ADMIN_PASSWORD in hub/.env.deploy).",
  );
  console.log(
    `     ${yellowWarn} postgres_admin_password was generated into parameters.local.json.`,
  );
  console.log("  3) Deploy infra with both parameter files:");
  console.log(
    "     az deployment group create --resource-group <rg> --template-file endatix-azure.template.bicep --parameters parameters.json --parameters parameters.local.json --mode Complete",
  );
  console.log("  4) Build and deploy API and Hub.");
  console.log("");
}

main().catch((error) => {
  console.error("Failed to generate quickstart secrets.");
  console.error(error);
  process.exit(1);
});
